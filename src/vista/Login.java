    private void txtUsuarioKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtUsuarioKeyTyped
        // TODO add your handling code here:
        int ncaracteres=5;
        char c= evt.getKeyChar();
        if((c<'a'||c>'z')&&(c<'A'||c>'Z'))evt.consume();
        if(txtUsuario.getText().length()>=ncaracteres)
        {
            evt.consume();
            JOptionPane.showMessageDialog(rootPane, "Usuario: solo 5 letras");
        }
       
    }//GEN-LAST:event_txtUsuarioKeyTyped

    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        vendedorDTO dTO= new vendedorDTO(null, null, null, null, null, null);
        if(aO.validarUser(dTO.getUsuario(),dTO.getContraseña()))
        {
            PrincipalForm pf= new PrincipalForm();
            pf.setVisible(true);
            this.hide();
        }
        else
        {
            JOptionPane.showMessageDialog(rootPane,"Usuario no existe");
        }
    }//GEN-LAST:event_btnLoginActionPerformed

    private void txtPasswKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPasswKeyTyped
        // TODO add your handling code here:
        int ncontraseña=4;
        if(new String(txtPassw.getPassword()).length()>=ncontraseña)
        {
            evt.consume();
            JOptionPane.showMessageDialog(rootPane, "Contraseña: solo 4 caracteres");
        }
    }//GEN-LAST:event_txtPasswKeyTyped

